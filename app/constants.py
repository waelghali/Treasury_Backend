    REL = "RL" # Release
    LIQ = "LQ" # Liquidation
    DEC = "DC" # Decrease Amount
    AMD = "AM" # Amendment
    ACT = "AC" # Activate Non-Operative
    DEL = "DL" # Record Delivery
    RPL = "RP" # Record Bank Reply
    REM = "RM" # Reminder to Banks
    CNC = "CN" # Cancel Last Instruction

